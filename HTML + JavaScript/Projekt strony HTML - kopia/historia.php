                            <h1 class="historia-tytuł">Lata 90'</h1>
                                <h2 class="historia-tytuł">Początki gier 3D</h2>
                                <b>Lata 90. były dekadą znaczących innowacji w grach wideo. Była to dekada przejścia od grafiki rastrowej do grafiki 3D</b>
                                <b>i dała początek kilku gatunkom gier wideo, w tym strzelankom pierwszoosobowym, strategiom czasu rzeczywistego i MMO. Gry przenośne stały się
                                    bardziej popularne w ciągu dekady, częściowo dzięki wydaniu Game Boy w 1989 roku.
                                    Gry zręcznościowe przeżyły odrodzenie na początku i w połowie lat 90-tych, a następnie spadek pod koniec lat 90-tych, gdy konsole domowe stały się bardziej powszechne.</b>
                                <p></p>
    
                                <h2 class="historia-tytuł">Upadek automatów do gier</h2>
                                <b>Jednym z czynników odciągającym graczy od salonów gier były najnowsze konsole domowe, które były teraz zdolne do grania w gry "arcade-accurate",
                                     w tym najnowsze gry 3D. Coraz większa liczba graczy czekała na przeniesienie popularnych gier arcade na konsole, zamiast wrzucać monety do automatów arcade.
                                     Trend ten nasilił się wraz z wprowadzeniem bardziej realistycznych urządzeń peryferyjnych do systemów gier komputerowych i konsolowych, takich jak joysticki lotnicze i zestawy kierownic/pedałów wyścigowych,
                                    które pozwoliły systemom domowym zbliżyć się do realizmu Aby pozostać istotnym, producenci gier arcade, ulepszali technogię gier 3D na automaty ale w końcu przegrali walkę o gracza.
                                    Sega ostatecznie zaprzestała produkcji niestandardowych płyt do systemów arcade, a ich kolejne płyty arcade były oparte na konsolach lub komercyjnych komponentach PC.</b>
                                    <p></p>
                                <h2 class="historia-tytuł">Konsole przenośne</h2>
                                    <b>W 1989 roku Nintendo wypuściło opartą na kartridżach konsolę Game Boy, pierwszą dużą konsolą przenośną od czasu Microvision dziesięć lat wcześniej.
                                         W zestawie z systemem znajdowała się gra Tetris, która stała się jedną z najlepiej sprzedających się gier wideo wszech czasów, przyciągając wiele osób, które normalnie nie grałyby w gry wideo na handhelda
                                         . Kilka innychh handheldów zadebiutowało na początku lat 90-tych, w tym Sega Game Gear i Atari Lynx (pierwszy handheld z kolorowym wyświetlaczem LCD). 
                                         Chociaż systemy te były bardziej zaawansowane technologicznie i miały dorównywać wydajnością konsolom domowym, zużywały więcej baterii .
                                         Podczas gdy niektóre z innych systemów pozostały w produkcji do połowy lat 90-tych, Game Boy i jego kolejne wcielenia, Game Boy Pocket, Game Boy Color i Game Boy Advance,
                                          były praktycznie niekwestionowanymi dominantami na rynku handheldów przez lata 90-te. Rodzina Game Boy wprowadziła również pierwsze odsłony serii Pokémon z Pokémon Red i Blue, 
                                          która pozostaje jedną z najlepiej sprzedających się serii gier wideo dla Nintendo.
                                     </b>




                                
