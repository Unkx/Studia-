                                    <h1 class="historia-tytuł">Lata 2010'</h1>
                                        <h2 class="historia-tytuł">Gry Multiplayer i mikro płatności</h2>
                                        <b>W ostatniej dekadzie wzrosło zainteresowanie rozwiązaniami opartymi na chmurze, które umożliwiają graczom strumieniowe granie bez potrzeby posiadania wysokiej mocy obliczeniowej w swoim sprzęcie.
                                            Usługi takie jak GeForce Now, PlayStation Now i Google Stadia dały graczom możliwość gry w zaawansowane gry na różnych urządzeniach, takich jak smartfony, tablety czy telewizory, za pomocą strumieniowania treści.
                                        </b>
                                        <p></p>
                                        <b>Dzięki dostępności do szybkiego internetu, gracze na całym świecie dostali bardziej płynne i stabilne połączenia. To z kolei otworzyło drzwi dla bardziej zaawansowanych funkcji sieciowych,
                                             takich jak wieloosobowe tryby gry online, kooperacja czy rywalizacja między graczami.</b>
                                        <p></p>
                                        <b>W latach 2010 wzrosła popularność modeli free-to-play (F2P), które oferują grę za darmo, ale zarabiają na sprzedaży przedmiotów w grze. Mikropłatności stały się powszechne, umożliwiając graczom zakup dodatkowych elementów,
                                            postaci, skórek czy ulepszeń. Model ten przyczynił się do rozwoju gier na urządzenia mobilne i przeglądarki internetowej.
                                        </b>
                                        <p></p>

                                        <h2 class="historia-tytuł">Grafika wysokiej rozdzielczości w sprzęcie do gier wideo</h2>
                                        <b>Wyświetlacze oparte na kineskopach zaczęły być stopniowo wycofywane w 2000 roku, zastępowane przez niedrogie płaskie telewizory i monitory, które miały znacznie wyższą rozdzielczość
                                             ekranu i częstotliwość odświeżania. Sprzęt do gier wideo zaczął wprowadzać wsparcie dla nowego standardu High-Definition Multimedia Interface (HDMI), umożliwiając rozdzielczość do 4K ,
                                              co wymagało wydajniejszych kart graficznych z szybszymi procesorami i większą pamięcią. Silniki gier, takie jak Unreal, Unity i DirectX, dodały obsługę ulepszonego mapowania tekstur w
                                               celu obsługi tekstur o wysokiej rozdzielczości, aby zapewnić fotorealistyczną grafikę w grach. Powstały też czujniki ruchowe takie jak Playstation Move czy Xbox Kinect ale porzucono ten pomysł
                                               jednakże sensory Kinect są używanie do dziś jako sensor laserowy do wykrywania przedmiotów w 3D, budowania map 2D, 2,5D i 3D, lokalizacji i nawigacji robotów mobilnych.
                                        </b>
                                        <p></p>
                                        <b>W komputerach osobistych rynek kart graficznych koncentrował się na postępach poczynionych przez liderów branży, firmy NVidia i AMD, które dostarczały również procesory graficzne do nowych konsol. 
                                            Dyski półprzewodnikowe (SSD), które w przeszłości były używane do przechowywania kart flash w konsolach do gier wideo, były na tyle zaawansowane, że stały się opcjami konsumenckimi do przechowywania 
                                            dużych ilości danych. W porównaniu do tradycyjnych dysków twardych (HDD), które wykorzystywały części elektromechaniczne, dyski SSD nie mają elementów mechanicznych i są zdolne do znacznie większej przepustowości 
                                            danych, co uczyniło je popularnymi opcjami dla komputerów zaprojektowanych do gier wideo.
