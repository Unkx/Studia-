                        <h2 class="historia-tytuł">Krach na rynku gier wideo</h2>
                        
                        <b>Krach gier wideo w Stanach Zjednoczonych w 1983 roku, znany również jako "Kryzys Atari", był okresem gwałtownego załamania się </b> 
                        <p></p>
                        <b>rynku gier wideo. Było to spowodowane szeregiem czynników, które miały wpływ na branżę w tamtym czasie.</b>
                        <p></p>
                        <b>Jednym z głównych czynników był nadmiar słabej jakości gier na rynku. Wielu producentów, w tym przede wszystkim Atari, wprowadzało na rynek gry o niskiej jakości,</b>
                        <p></p>
                        <b>które często były niedopracowane i niezadowalające dla graczy. W rezultacie klienci tracili zaufanie do gier wideo i zniechęcali się do ich zakupu.</b>

                        <h2 class="historia-tytuł">Rozwój gier arcade'owych</h2>
                        <b>Wzrost popularności gier arkadowych: W latach 80. gry arkadowe odgrywały ogromną rolę w przemyśle gier wideo. Automaty do gier, takie jak "Pac-Man", "Space Invaders" i "Donkey Kong",
                        przyciągały rzesze graczy do salonów gier. To była dekada, w której gry arkadowe zdobyły ogromną popularność i stały się jednym z symboli tamtego czasu.</b>
                        <p></p>
                        <h2 class="historia-tytuł">Początek gier online</h2>
                        <b>Początki gier na komputery osobiste: W latach 80. nastąpił rozkwit komputerów osobistych, co otworzyło nowe możliwości dla rozwoju gier wideo. </b>
                        <p></p>
                        <b>Powstały takie kultowe platformy jak Commodore 64, ZX Spectrum czy Atari ST. Na tych komputerach pojawiły się gry, które zyskały ogromną popularność, takie jak "Castle Wolfenstein", "SimCity" czy "Prince of Persia".</b>
                        <h2 class="historia-tytuł">Narodziny legend</h2>
                        <b>Narodziny popularnych postaci w świecie gier: W latach 80. pojawiły się wiele ikonicznych postaci, które stały się legendami branży gier wideo. Mario, Sonic the Hedgehog, Link, Samus Aran, 
                        <b>Mega Man i wiele innych postaci zyskało ogromną popularność i stało się symbolem tamtego okresu.</b>
                        <p></p>
                        <b></b>Powstanie gatunków i tytułów kultowych gier: W latach 80. powstały gatunki gier, które do dzisiaj są popularne. Gry platformowe, takie jak "Super Mario Bros." i "Mega Man",</b>
                        <p></p>
                        <b>gry RPG, takie jak "The Legend of Zelda" i "Final Fantasy", oraz gry akcji, takie jak "Contra" i "Metal Gear", zdobyły ogromną popularność i miały wpływ na dalszy rozwój przemysłu gier wideo.
                        </b>
                        <p></p>


