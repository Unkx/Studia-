                                <h1 class="historia-tytuł">Lata 00'</h1>
                                    <h2 class="historia-tytuł">Gry AAA i pojawienie się gier niezależnych</h2>
                                    <b>W 2000 roku twórcy gier zaczęli otrzymywać coraz większe budżety na rozwój. Final Fantasy VII miało szacunkowy budżet w wysokości 40-45 milionów dolarów bez uwzględnienia marketingu,
                                        podczas gdy pierwsza gra Shenmue kosztowała 47-70 milionów dolarów. Więksi deweloperzy zaczęli podchodzić do gier porównywalnie do hollywoodzkich produkcji filmowych, nie tylko biorąc
                                         pod uwagę aspekty rozwoju, dystrybucji i marketingu, ale także uwzględniając budżety zarówno na kinematografię w grze, w tym profesjonalnych aktorów i licencjonowane nieruchomości, jak i 
                                         większe elementy promocyjne. Te nowe podejścia jeszcze bardziej rozszerzyły budżety gier. Podobnie jak w przypadku przebojowych filmów, branża gier wideo zaczęła nazywać te wysokobudżetowe
                                          gry oraz stojących za nimi wydawców i deweloperów mianem "AAA" lub "triple A" pod koniec lat 90. i na początku XXI wieku.</b>
                                        <p></p>

                                    <h2 class="historia-tytuł">Gry przeglądarkowe</h2>
                                    <b>Pod koniec lat 90. i na początku XXI wieku nastąpił rozkwit dostępności Internetu i nowych technologii internetowych, takich jak Java i Adobe Flash. Chociaż Adobe Flash początkowo miał być 
                                    narzędziem do tworzenia w pełni interaktywnych stron internetowych, Flash stracił przychylność w tym obszarze, ale indywidualni programiści znaleźli sposoby na wykorzystanie tego narzędzia do animacji i gier,
                                     wspomaganych przez łatwość narzędzi programistycznych do tego celu.
                                     Wraz z rozwojem serwisów społecznościowych, na platformach społecznościowych pojawiły się pierwsze gry społecznościowe. Gry te, często oparte na mechanice gier casualowych, zazwyczaj polegają na interakcji użytkowników
                                    ze znajomymi za pośrednictwem serwisu społecznościowego, aby uzyskać formę "energii" do dalszej gry.
                                    </b>
                                    <p></p>
                                    <b>
                                        Niestety w 2021 Adobe wyłączyło wtyczkę Flash i spowodowało "śmierć" gier flashowych.
                                    </b>
                                        <p></p>

                                    <h2 class="historia-tytuł">Gry mobilne</h2>
                                        <b>W około 2005 roku na rynku pojawiły się pierwsze smartfony, które oferowały łączność danych wraz z usługami telefonicznymi.
                                             W 2007 r. Apple, Inc. wprowadził iPhone'a, który był technologicznie bardziej zaawansowany niż inne smartfony na rynku, a w 2008 r. zaprezentował App Store,
                                             za pośrednictwem którego można było kupować nowe aplikacje. Dzięki App Store deweloperzy, po zarejestrowaniu się jako partner, mogli następnie opracowywać i publikować własne 
                                             aplikacje za pośrednictwem sklepu. Pozwoliło to deweloperom dowolnej wielkości uczestniczyć w rynku App Store. Google, który opracował konkurencyjny mobilny system operacyjny Android,
                                              wydał własną wersję sklepu z aplikacjami w 2008 roku, później nazwaną Google Play w 2012 roku.
                                        </b>
    
    
    
    
                                    
